# 入力の受け取り
N = int(input())  # 宇宙人の人数
A = list(map(int, input().split()))  # 各宇宙人の初期の石の個数

# C[i] は成人するまでに受け取った石の累積和
C = [0] * N

# D[i] は差分配列。石の受け渡しの開始・終了を管理する
D = [0] * (N + 1)

# 各宇宙人が成人する順番に処理
for i in range(N):
    # 成人するごとに、これまでの累積受け取り数を計算して現在の石に加算
    if i != 0:
        C[i] = C[i - 1] + D[i]  # 前回の累積値に差分を加算
        A[i] += C[i]  # 累積で受け取った石を現在の石に加える

    # 石を渡せる最大人数を計算
    cnt = min(N - i - 1, A[i])  # i番目の宇宙人が渡せる人数（残りの未成人人数との最小値）
    A[i] -= cnt  # 渡した分の石を減らす

    # 差分配列 D を更新して、石の受け渡しの開始・終了を管理
    D[i + 1] += 1  # 石の受け渡し開始位置
    D[min(N, i + cnt + 1)] -= 1  # 石の受け渡し終了位置

# 最終的な石の個数を出力
print(*A)